function remove(n){
    var replacement = nextBestCard(n);
    document.getElementById('card'.concat(n)).src = replacement;
    saveOldBand();
    band[n] = replacement;
}

function undo(){
    for(var i = 0; i < 5; i++){
        band[i] = oldBand[i];
        document.getElementById('card'.concat(i)).src = oldBand[i];
    }
}

